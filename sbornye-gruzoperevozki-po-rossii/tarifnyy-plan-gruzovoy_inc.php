<p align="justify"><font size="2" face="Verdana" color="#262626"><span class="LineHeight1-5"><span style="font-size: 14px;"><strong>Грузовые перевозки в крытых вагонах</strong> - это  самый удобный и выгодный способ для транспортировки тарно-штучных  грузов широкой номенклатуры. Также в крытых вагонах выполняются железнодорожные перевозки опасных грузов (лаки, краски, огнетушители и т.д.)</span></span></font></p>
 
