  <p align="justify"><font size="2" face="Verdana" color="#363636"><span class="LineHeight1-5">Грузовые вагоны железнодорожного транспорта подразделяют на универсальные и специализированные.</span></font></p>
 
  <div align="justify"><font size="2" face="Verdana" color="#363636"><span class="LineHeight1-5"> </span></font></div>
 
  <p align="justify"><font size="2" face="Verdana" color="#363636"><span class="LineHeight1-5">К <strong>универсальным</strong> относятся вагоны основных типов (крытые, полувагоны, платформы), предназначенные для перевозки широкой номенклатуры грузов.</span></font></p>
 
  <div align="justify"><font size="2" face="Verdana" color="#363636"><span class="LineHeight1-5"> </span></font></div>
 
  <p align="justify"><font size="2" face="Verdana" color="#363636"><span class="LineHeight1-5">К <strong>специализированным</strong> относятся вагоны разных типов, имеющие специальную конструкцию, предназначенные для перевозки одной или нескольких групп грузов, требующих специальных условий перевозки, погрузки и выгрузки, имеющие особые формы кузова, устройства, оборудования и приспособления.</span></font></p>
 
