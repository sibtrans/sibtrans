   </font></p>
 
<p class="MsoNormal"><font size="4" face="Tahoma" color="#790000"></font> 
  <br />
 <span style="font-size: 10pt; line-height: 115%;"> </span> </p>
 
<p class="MsoNormal"><font size="2" face="Verdana" color="#262626"><span class="LineHeight1-5">Самый быстрый и безопасный из наземных видов транспорта. Доставка осуществляется в составе сборного груза в почтово багажных и грузовых вагонах.</span></font></p>
 
<p class="MsoNormal"><font size="3" face="Tahoma"><span class="LineHeight1-5"> 
      <br />
     </span></font></p>
 
<p class="MsoNormal"><font size="3" face="Tahoma"><span class="LineHeight1-5"> 
