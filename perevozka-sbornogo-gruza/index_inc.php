 <span style="font-size: 10pt; line-height: 115%;"></span><font color="#262626"><b><font size="3" face="Tahoma"><span class="LineHeight1-5">Доставка груза в почтово багажных вагонах:</span></font></b><span style="font-size: 10pt; line-height: 115%;"></span><font size="3" face="Tahoma"><span class="LineHeight1-5"> </span></font></font> 
<p class="MsoNormal" align="justify"><font size="3" face="Tahoma" color="#262626"><span class="LineHeight1-5"><font size="2" face="Verdana">Самый быстрый вид доставки груза из наземных видов транспорта. Отправка вагонов осуществляется 4 дня в неделю в составе сборного груза.  </font> 
      <br />
     </span></font></p>
 
<p class="MsoNormal"><font size="3" face="Tahoma" color="#262626"><span class="LineHeight1-5"> 
      <br />
     </span></font></p>
 
<p class="MsoNormal"><font size="3" face="Tahoma" color="#262626"><span class="LineHeight1-5"> 
      <br />
     </span></font></p>
 <font size="3" face="Tahoma" color="#262626"><span class="LineHeight1-5"> </span></font> 
<p class="MsoNormal"><font color="#262626"><b><font size="3" face="Tahoma"><span class="LineHeight1-5">Отправка груза клиентом осуществляется по следующей схеме:</span></font></b></font></p>
 
<p class="MsoNormal"><font color="#262626"><b><font size="3" face="Tahoma"><span class="LineHeight1-5"> 
          <br />
         </span></font></b></font></p>
 <font size="3" face="Tahoma" color="#262626"><span class="LineHeight1-5"> </span></font> 
<p class="MsoNormal" style="margin-bottom: 0.0001pt;"><font size="2" face="Verdana" color="#262626"><span class="LineHeight1-5">1) Оформление заявки и Бронирование места в транспортном средстве (не менее чем за 24 часа);</span></font></p>
 <font size="2" face="Verdana"> <font color="#262626"><span class="LineHeight1-5"> </span></font> </font> 
<p class="MsoNormal" style="margin-bottom: 0.0001pt;"><font size="2" face="Verdana" color="#262626"><span class="LineHeight1-5">2) Сдача груза для его дальнейшей транспортировки (документы необходимые при сдаче груза);</span></font></p>
 <font size="2" face="Verdana"> <font color="#262626"><span class="LineHeight1-5"> </span></font> </font> 
<p class="MsoNormal" style="margin-bottom: 0.0001pt;"><font size="2" face="Verdana" color="#262626"><span class="LineHeight1-5">3) Получение груза в городе назначения. </span></font></p>
 <font size="3" face="Tahoma" color="#262626"><span class="LineHeight1-5"></span></font><font size="3" face="Tahoma" color="#262626"><span class="LineHeight1-5"> 
    <br />
   </span></font> 
