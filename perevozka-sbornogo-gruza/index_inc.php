<p class="MsoNormal" align="justify"><font size="2" face="Verdana" color="#262626"><span class="LineHeight1-5">Самый доступный и экономичный вид транспорта. Отправка осуществляется по всей территории России с ежедневной отправкой, в составе сборных грузов размещенных в одном транспортном средстве. </span></font><span style="font-size: 10pt; line-height: 115%;"><font size="2" face="Verdana"> </font> 
