     </span></font></p>
 
<p class="MsoNormal"><font size="3" face="Tahoma" color="#262626"><span class="LineHeight1-5"><b>Доставка груза в грузовых вагонах:</b></span></font><font size="3" face="Tahoma" color="#262626"><span class="LineHeight1-5"></span></font><font size="3" face="Tahoma" color="#262626"><span class="LineHeight1-5"> 
      <br />
     </span></font></p>
 
<p class="MsoNormal" align="justify"><font size="2" face="Verdana" color="#262626"><span class="LineHeight1-5">Доставка груза осуществляется грузовыми, крытыми вагонами. Выбрав данную категорию, Вы получаете минимальные расходы на доставку своего товара и возможность четкого планирования получения груза</span></font></p>
 
