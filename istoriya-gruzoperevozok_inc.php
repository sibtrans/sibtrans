 </div>
 
<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>
 
<div align="justify"> 
  <p> </p>
 
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Следующей важной вехой в истории грузоперевозок стало распространение железной дороги. Первой наземной дорогой считается «Уоллатонская вагонная дорога». Это железная дорога, длиной примерно три километра, была построена между 1603 и 1604 годами для перевозки угля между поселками Стрелли и Уоллатон в Великобритании. Дальнейшее изобретение автомобиля значительно облегчило процесс доставки грузов и стало началом новой эры в развитии грузоперевозок. </span></font></p>
 
  <p> </p>
 
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">В США, Мексике и других странах особой важностью стала защита груза от нападений. Грузовые дворы Америки нанимали специальные отряды для защиты от банд дикого Запада, ведь тогда наполовину транпортируемый груз состоял из вооружения и боеприпасов, наполовину из золота. За особо ценные грузы разгорались сражения и перестрелки. </span></font></p>
 
  <p> </p>
 
  <p>
    <br />
  </p>
 
  <p> </p>
 
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">   Появление самолетов стало важным моментом в истории грузоперевозок. Первый полет   
        <br />
       </span></font></p>
 
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">   самолета в истории был осуществлен 17   декабря 1903 года. Аппарат продержался в 
        <br />
       </span></font></p>
 
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">   воздухе 59 секунд и пролетел 260 метров. После этого самолеты стали реконструировать 
        <br />
       </span></font></p>
 
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">   и дорабатывать. Появление гражданской авиации позволило доставлять грузы на огромные расстояния за короткое время. </span></font></p>
 
