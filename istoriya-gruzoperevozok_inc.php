<p align="center"><font size="5" face="Verdana" color="#9D0A0F"><span class="LineHeight1-5">История грузоперевозок</span></font></p>
 
<p align="center"><font size="5" face="Verdana" color="#9D0A0F"><span class="LineHeight1-5"> 
      <br />
     </span></font></p>
 
<p><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>
 
<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">Перевозка груза во все времена имела важное значение для экономики государств и отдельного человека. Именно поэтому история грузоперевозок началась очень много веков назад.</span></font></p>
 
<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<br />
 
<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>
 
<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">   Началом истории грузоперевозок можно считать изобретение колеса в 4-м тысячелетии до н.э.  
      <br />
     </span></font></p>
 
<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">   Первоначально колесо представляло собой деревянный диск, насаженный на ось и 
      <br />
     </span></font></p>
 
<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">   зафиксированный клином. Позже, во 2-м тысячелетии до н.э., конструкция совершенствуется: 
      <br />
     </span></font></p>
 
<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">   появляется колесо со спицами, ступицей и гнутым ободом. В 1-м тысячелетии до н.э. кельты для увеличения прочности колес своих колесниц стали применять металлический обод, который затем в транспортных машинах был заменен резиновыми шинами для амортизации. Для перевозки грузов в древности использовали мускульную силу животных – быков, лошадей, верблюдов, оленей и собак. </span></font></p>
 
<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<br />
 
<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>
 
<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">Долгое время люди для перевозки товаров пользовались шелковым путем. Это была целая система древних торговых караванных дорог, ведущих из Китая в страны Ближнего Востока и Европы, успешно функционировавшая вплоть до XIV века. По Шелковому пути везли шелк, восточные специи, фарфор и другие экзотические товары.</span></font></p>
 
<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>
 
<div align="justify"> 
  <p> </p>
 
  <p><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>
 
  <p> </p>
 
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">С развитием мореплавания человек смог доставлять грузы на дальние расстояния и в большом количестве. </span></font></p>
 
  <p> </p>
 
  <p>
    <br />
  </p>
 
  <p> </p>
 
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">   В XVI веке начали использовать дилижансы – многоместные кареты, запряженные 
        <br />
       </span></font></p>
 
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">   лошадьми. На дорогах начали появляться почтовые станции, где меняли уставших лошадей 
        <br />
       </span></font></p>
 
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">   по принципу эстафеты. Позже лошадей стали заменять паровыми машинами. Первая такая 
        <br />
       </span></font></p>
 
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">   машина имела мощность 40 лошадиных сил. </span></font></p>
 
  <p> </p>
 </div>
 
<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>
 
<div align="justify"> 
