  <div align="justify"> 
    <p><font size="2" face="Verdana"><span class="LineHeight1-5">История грузоперевозок в России тесно связана со становлением и развитием русской почты. Основы почтовых услуг начали закладываться еще в самом начале Киевской Руси – в первой четверти IX века. Уже в те времена гонцы доставляли письма и личные вещи в разные концы русских земель.</span></font></p>
   </div>
 
  <p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>
 
  <p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">            Следующие перевозки в России были водными. Русское государство – это, в основном, непроходимые леса. Значительный сдвиг был достигнут в середине XIX века. В это время был изобретен паровой двигатель, а потом и железная дорога. Благодаря этому стало возможно перевозить большое количество грузов на дальние расстояния в любое время года.</span></font></p>
 
  <p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>
 
  <p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">            В течение XX века и по сей день история грузоперевозок представляет собой совершенствование технологий доставки, предоставление новых возможностей клиентам и формирование комплексного подхода к транспортировке груза. 
