<p><b><font size="2" face="Verdana"><span class="LineHeight1-5"> <font color="#262626">Примечание:</font></span></font></b></p>
 <font color="#262626"> </font> 
<p><font size="2" face="Verdana" color="#262626"><span class="LineHeight1-5"> </span></font></p>
 <font color="#262626"> </font> 
<p><font size="2" face="Verdana" color="#262626"><span class="LineHeight1-5"> </span></font></p>
 <font color="#262626"> </font> 
<p><font size="2" face="Verdana" color="#262626"><span class="LineHeight1-5"> </span></font></p>
 <font color="#262626"> </font> 
<ol> 
  <li style="text-align: justify;"> 
    <p><font size="2" face="Verdana" color="#262626"><span class="LineHeight1-5">Груз на сборный склад в г.Урумчи принимается только по заявке / уведомлению.</span></font></p>
   </li>
 
  <li style="text-align: justify;"> 
    <p><font size="2" face="Verdana" color="#262626"><span class="LineHeight1-5">Согласование товаросопроводительных документов перед отправкой груза обязательно.</span></font></p>
   </li>
 
  <li style="text-align: justify;"> 
    <p><font size="2" face="Verdana" color="#262626"><span class="LineHeight1-5">В тариф на перевозку груза входит: прием и хранение груза на складе г.Урумчи, подготовка товаросопроводительных документов, проверка экспортных документов, транспортировка груза до г.Новосибирск, ПРР на приграничном терминале, оформление транзитной документации.</span></font></p>
   </li>
 
  <li style="text-align: justify;"> 
    <p><font size="2" face="Verdana" color="#262626"><span class="LineHeight1-5">Дополнительные услуги (по заявке): поиск и приобретение товара в КНР, заключение контракта, оплата товара,  контроль погрузки, экспедирование товара по территории Китая, таможенное оформление в Китае и России, услуги СВХ в Новосибирске, адресная доставка грузов по территории Российской Федерации.</span></font></p>
   </li>
 
  <li style="text-align: justify;"> 
    <p><font size="2" face="Verdana" color="#262626"><span class="LineHeight1-5">Расширение сферы деятельности: (по согласованию) организация перевозки грузов в/через Казахстан, с таможенным оформлением в РК.</span></font></p>
   </li>
