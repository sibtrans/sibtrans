    <li style="text-align: justify;"> 
      <p><font size="2" face="Verdana" color="#262626"><span class="LineHeight1-5">доставка грузов под наш контракт; при этом клиент не включается во внешнеэкономическую деятельность, наша компания выступает контрактодержателем, проводится экспортное оформление и таможенное оформление в РФ нашими силами; на выходе клиент получает таможенно чистый груз с набором необходимых документов;</span></font></p>
     </li>
   </ul>
