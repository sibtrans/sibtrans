  <div align="center"><font color="#790000"><font size="5" face="Tahoma">Таможенное оформление</font></font> 
    <br />
   </div>
 <font color="#790000"> 
    <br />
   </font> 
  <h2 style="text-align: center;"><font size="2" face="Verdana"> </font></h2>
 <font size="2" face="Verdana"></font> 
  <p style="margin-bottom: 0in;" align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">Все коммерческие партии товаров, перевозимые через границу, подлежат таможенному контролю. Перемещение грузов осуществляется с соблюдением нормативных требований. Основным правилом таможенного оформления является предоставление корректных и достоверных сведений о товаре. При несоблюдении данного условия товар может быть задержан, а на получателя наложена административная ответственность. Таможенное оформление может проводиться либо непосредственно владельцем (декларантом) груза, либо таможенным представителем, действующим от имени и по поручению декларанта.</span></font></p>
 
  <p style="margin-bottom: 0in;" lang="en-US" align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> 
        <br />
       </span></font></p>
 
  <p style="margin-bottom: 0in;" lang="en-US"><font size="2" face="Verdana"><span class="LineHeight1-5"> 
        <br />
       </span></font> </p>
 
  <p style="margin-bottom: 0in;"><font size="3" color="#790000"><b><font face="Verdana"><span class="LineHeight1-5">Список необходимых документов для таможенного оформления:</span></font></b></font> </p>
 
  <p style="margin-bottom: 0in;"><font size="2" face="Verdana"><span class="LineHeight1-5"> 
        <br />
       </span></font> </p>
 
  <ul> 
    <li> 
      <div align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">Внешнеторговый договор, соглашение на поставку и др. </span></font> </div>
     </li>
   
    <li> 
      <div align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">Товарно-транспортные документы (накладные, коносамент, книжка МДП). - Документы, на основании которых был заявлен классификационный код товара. </span></font> </div>
     </li>
   
    <li> 
      <div align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">Документы, подтверждающие уплату таможенных платежей. </span></font> </div>
     </li>
   
    <li> 
      <div align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">Рублевые или валютные платежные поручения. </span></font> </div>
     </li>
   
    <li> 
      <div align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">Для валютного контроля и определения таможенной стоимости товара – паспорт сделки, а также документы, подтверждающие заявленную таможенную стоимость товаров. </span></font> </div>
     </li>
   
    <li> 
      <div align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">Документы, подтверждающие соблюдение запретов, ограничений и мер нетарифного регулирования (лицензии на импорт-экспорт товаров, сертификаты безопасности, гигиенические сертификаты, ветеринарные или фитосанитарные свидетельства, сертификаты соответствия и т.д.).</span></font></div>
     </li>
   
    <li> 
      <div align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">Доверенность на лицо, которое представляет организацию в таможенных органах. </span></font> </div>
     </li>
   
    <li> 
      <div align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">Данные о компании (пакет уставных документов, свидетельство из налоговой инспекции о постановке на учет, свидетельство о внесении в Единый государственный реестр, выписка (коды статистики), банковская справка о наличии расчетного счета). </span></font> </div>
     </li>
   </ul>
 
  <p style="margin-bottom: 0in;" align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> 
        <br />
       </span></font></p>
 
  <p style="margin-bottom: 0in;" align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> 
        <br />
       </span></font> </p>
 
  <p style="margin-bottom: 0in;" align="justify"><font size="2" face="Verdana" color="#790000"><span class="LineHeight1-5"><b><font size="3">В случае импорта необходимы:</font></b> <font color="#262626">Экспортная декларация. При наличии – сертификат формы «А», «СТ-1». Прайс-лист, предоставляемый отправителем. </font></span></font> </p>
 
  <p style="margin-bottom: 0in;" align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> 
        <br />
       </span></font></p>
 
  <p style="margin-bottom: 0in;" align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> 
        <br />
       </span></font> </p>
 
  <p style="margin-bottom: 0in;" align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"><font color="#790000"><b><font size="3">В случае экспорта необходимы:</font></b></font> Счета-фактуры, счета-проформы, контракт о заключении внешнеэкономической сделки. Контракт на закупку на территории РФ. Спецификации, упаковочные листы. TIR, CMR. Техническая документация на товар с подробными характеристиками и серийными номерами. </span></font> </p>
 
  <p style="margin-bottom: 0in;" align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> 
        <br />
       </span></font></p>
 
  <p style="margin-bottom: 0in;" align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> 
        <br />
       </span></font> </p>
 
  <p style="margin-bottom: 0in;" align="justify"><i><font size="2" face="Verdana" color="#790000"><span class="LineHeight1-5">Комплект документов определяется характером груза. Выше перечислены лишь основные документы, необходимые для таможенного оформления.</span></font></i></p>
 
