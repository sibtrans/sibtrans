<p style="text-align: justify;" align="justify"><font face="Tahoma"><span class="LineHeight1-5">Новый год, когда компания привозит праздничные наборы к новогоднему столу – здесь и красная икра, и пельмени, и мандарины, и ананасы – все то, что дома припасают на праздник.</span></font></p>
 
