<p style="text-align: justify;" align="justify"><font face="Tahoma"><span class="LineHeight1-5">День Знаний 1 сентября также является поводом для встреч. Книги, игрушки, билеты в кино и обязательно сладости.</span></font></p>
 
<div align="justify"><font face="Tahoma"><span class="LineHeight1-5"> </span></font></div>
 
<p style="text-align: justify;" align="justify"><font face="Tahoma"><span class="LineHeight1-5">В 2009 году группа компании «БТК» участвовала в реализации госзаказа для Новосибирского театра оперы и балета — организовывала и перевозила реквизит театра (гастроли в Корее и Франции) </span></font></p>
 
<div align="justify"><font face="Tahoma"><span class="LineHeight1-5"> </span></font></div>
 
<p style="text-align: justify;" align="justify"><font face="Tahoma"><span class="LineHeight1-5">Весной 2012 года была осуществлена доставка социально-важного груза – гуманитарная помощь из Германии в строящийся Дом Милосердия (Академгородок). Данный груз (функциональные кровати с матрасами и прикроватные тумбочки) &quot;Мы любим нашу компанию&quot; предназначен для организации ухода за одинокими пожилыми людьми и инвалидами.</span></font></p>
 
<div align="justify"><font face="Tahoma"><span class="LineHeight1-5"> </span></font></div>
 
<p style="text-align: justify;" align="justify"><font face="Tahoma"><span class="LineHeight1-5">В 2013 компания «Байт-Транзит-Континент» не оставаясь в стороне, от горя других людей и оказывала благотворительную помощь в доставке гуманитарной помощи районам  Дальнего Востока пострадавшим от  наводнения.</span></font></p>
 
