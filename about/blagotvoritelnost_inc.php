<h2 style="text-align: justify;" align="justify"><font size="3" face="Tahoma" color="#790000"><span class="LineHeight1-5"> Компания &quot;Байт Транзит&quot; - это активный деятель в жизни города Новосибирск, со дня основания компания взяла курс на открытость и взаимодействие со всеми структурами нашего города, в том числе с детскими и благотворительными организациями. Компания старается всегда участвовать во всех благотворительных мероприятиях, поддерживать и помогать детским учреждениям.</span></font></h2>
 
