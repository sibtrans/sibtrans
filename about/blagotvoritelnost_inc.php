<p style="text-align: justify;" align="justify"><font face="Tahoma"><span class="LineHeight1-5">В 1997 году «Байт-Транзит-Континент» стал одним из исполнителей спонсорской программы «Дети Японии – Детям Сибири». Был принят самолет с большим грузом 198 кубов медикаментов и в течении одной ночи &quot;Мы любим нашу компанию&quot; развезены по детским областным клиникам Западно-Сибирского региона: Новосибирск, Томск, Омск, Кемерово, Барнаул, Бийск.</span></font></p>
 
<div align="justify"><font face="Tahoma"><span class="LineHeight1-5"> </span></font></div>
 
<p style="text-align: justify;" align="justify"><font face="Tahoma"><span class="LineHeight1-5"> </span></font></p>
 
<div align="justify"><font face="Tahoma"><span class="LineHeight1-5"> </span></font></div>
 
<p style="text-align: justify;" align="justify"><font face="Tahoma"><span class="LineHeight1-5">С 2004 года и  на протяжении последних лет «Байт-Транзит-Континент» помогает Детскому дому № 7. Начиналась дружба с маленькой просьбы в 2004 году – перевезти печь для кухни. Потом надо было вывезти мусор, отремонтировать вентиляцию, бассейн, актовый зал, оплатить дорожные расходы для воспитанницы в лагерь «Океан» — и вновь помогает «Байт-Транзит-Континент». </span></font></p>
 
