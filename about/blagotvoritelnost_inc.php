<p style="text-align: justify;" align="justify"><font face="Tahoma"><span class="LineHeight1-5">История сохранила тот факт, что еще в 1995 году были доверены компании перевозки негабаритного оборудования для нефтяных компаний в северные районы Новосибирской области и доставка компьютерного оборудования объемом до 15 тысяч куб.м. в рамках государственного заказа Центризбиркома России – Государственной автоматизированной системы «Выборы».</span></font></p>
 
<div align="justify"><font face="Tahoma"><span class="LineHeight1-5"> </span></font></div>
 
<p style="text-align: justify;" align="justify"><font face="Tahoma"><span class="LineHeight1-5"> </span></font></p>
 
<div align="justify"><font face="Tahoma"><span class="LineHeight1-5"> </span></font></div>
 
<p style="text-align: justify;" align="justify"><font face="Tahoma"><span class="LineHeight1-5">В последние годы достаточно широко нашими услугами пользуются государственные и акционированные заводы города Новосибирска: завод «Электросигнал», «БЭМЗ», завод «Экран», НЭВЗ, научно-исследовательские институты Сибирского отделения Российской академии наук.  «Байт-Транзит-Континент» регулярно выполняет заказы Института ядерной физики РАН, Института катализа, перевозя необходимое им оборудование и комплектующие.</span></font></p>
 
<div align="justify"><font face="Tahoma"><span class="LineHeight1-5"> </span></font></div>
 
<p style="text-align: justify;" align="justify"><font face="Tahoma"><span class="LineHeight1-5"> </span></font></p>
 
<div align="justify"><font face="Tahoma"><span class="LineHeight1-5"> </span></font></div>
 
<p style="text-align: justify;" align="justify"><font face="Tahoma"><span class="LineHeight1-5">Для  ЗАО НПП «Медтехника»  завозит  медицинское оборудование для переоснащения районных больниц и эту работу они доверяют только «Байт Транзит Континент».</span></font></p>
 
<div align="justify"><font face="Tahoma"><span class="LineHeight1-5"> </span></font></div>
 
<p style="text-align: justify;" align="justify"><font face="Tahoma"><span class="LineHeight1-5">У нас есть опыт оформления всех необходимых документов, опыт работы с самыми сложными грузами — негабаритными, дорогостоящими, требующими особых условия для транспортировки и хранения. Мы хорошо понимаем, что бережное отношение к грузу — это бережное отношение к собственной репутации. А ей мы очень дорожим.</span></font></p>
 
