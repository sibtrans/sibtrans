<p><font size="5" color="#790000" face="Tahoma">Оценка условий труда</font></p>
 <font face="Tahoma"> <font color="#262626"> 
    <br />
   </font></font><font color="#262626"> </font> 
<div align="justify"><font face="Tahoma"><span class="LineHeight1-5"><font color="#262626">Обеспечение достойных условий труда работников является одной из основных задач компании &quot;Байт Транзит&quot;, в рамках действующей системы управления охраной труда, при этом ключевую роль играют результаты специальной оценки условий труда, которая проводится в соответствии с требованиями Федерального закона от 28.12.2013 № 426-ФЗ. В данном разделе представлены сводные данные о результатах проведения специальной оценки условий труда, на рабочих местах которых проводилась специальная оценка условий труда.</font> 
