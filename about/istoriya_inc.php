<p><font color="#790000"><span class="LineHeight1-5"><span style="font-size: 16px;"><strong>Компания «Байт-Транзит-Континент»</strong> («БТК») </span></span></font></p>
 
<p><font color="#790000"><span class="LineHeight1-5"><span style="font-size: 16px;"> 
        <br />
       </span></span></font></p>
 
<p><font size="3" face="Tahoma"><span class="LineHeight1-5"> </span></font></p>
 
<p align="justify"><font size="3" face="Tahoma"><span class="LineHeight1-5"><span style="font-size: 14px;">       - берет свое начало с 1991 года. Тогда компания состояла из 6 единомышленников и нескольких арендованных вагонов. Со дня основания, руководством в своей работе был сделан упор на профессионализм и качество оказываемых услуг. Выбранная политика позволила с каждым годом успешно развивать и увеличивать как размер компании, так и количество постоянных клиентов. Уже в 1993 году <strong>«БТК»</strong> открывает дочернюю компанию <strong>«Байт-Транзит-Экспедицию»</strong>, направлением которой становится контейнерные грузоперевозки. В этом же году происходит открытие филиала в Москве, что позволяет расширить границы работы организации.</span></span></font></p>
 
<p align="justify"><font size="3" face="Tahoma"><span class="LineHeight1-5"> </span></font></p>
 
<p align="justify"><font size="3" face="Tahoma"><span class="LineHeight1-5"><span style="font-size: 14px;">        С 1994 года <strong>«БТК»</strong> начинает приобретать собственный парк вагонов и автотранспорта, расширять филиальную сеть. Так же компания начала расширять и приобретать складские помещения, таким образом,  к 2000 году в г. Новосибирск у <strong>«БТК»</strong> был собственный гаражный комплекс, построен транспортный терминал, а так же открыт собственный склад временного хранения.</span></span></font></p>
 
<p align="justify"><font size="3" face="Tahoma"><span class="LineHeight1-5"> </span></font></p>
 
<p align="justify"><font size="3" face="Tahoma"><span class="LineHeight1-5"><span style="font-size: 16px;">      <span style="font-size: 14px;"> На сегодняшний день <strong>«БТК»</strong> это часть большого транспортно логистического холдинга, преуспевающее развитое предприятие, штат которого насчитывает более 200 единиц собственной техники, более 700 сотрудников и 17 филиалов по всей России. </span></span><span style="font-size: 16px;"><span style="font-size: 14px;">Ежедневно происходят отправки грузов автомобильным и железнодорожным транспортом  в том числе и на Дальний Восток. За эти годы «БТК» снискал себе репутацию надежного партнера, которому доверяют сотни тысяч клиентов.</span></span></span></font></p>
 
