<p style="text-align: justify;" align="justify"><font color="#9D0A0F"><b><font size="2" face="Verdana"><span class="LineHeight1-5">ГРУППА D</span></font></b></font></p>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana" color="#9D0A0F"><span class="LineHeight1-5">DAF Delivered At Frontier Поставка до границы (Название Места Поставки) </span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">«Поставка до границы» означает, что обязанности продавца по поставке товара считаются выполненными, когда товар, очищенный от пошлин для вывоза, прибывает в указанный пункт или место на границе, однако до  поступления товара на таможенную границу принимающей страны. Под термином граница понимается любая граница, включая границу страны экспорта. Поэтому в данном условии весьма важно точное определение границы путем указания на конкретный пункт или место.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Данный термин предназначен для применения главным образом при перевозке товара по железной дороге или автомобильным транспортом, но он может быть применен и при любом другом способе перевозки товара.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana" color="#9D0A0F"><span class="LineHeight1-5">DES Delivered Ex Ship Поставка с судна (Название Порта Назначения) </span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">«Поставка с судна» означает, что продавец считается выполнившим свои обязанности по поставке товара с момента предоставления неочищенного от таможенных пошлин при ввозе товара в распоряжение покупателя в согласованном порту назначения. Все расходы и риски по доставке товара в согласованный порт назначения несет продавец.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Данный термин может использоваться только при перевозках товара морским транспортом или внутренним водным транспортом.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana" color="#9D0A0F"><span class="LineHeight1-5">DEQ Delivered Ex Quay Поставка с пристани (Название Порта Назначения) </span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">«Поставка с пристани (с оплатой пошлины)» означает, что продавец считается выполнившим свои обязанности по поставке товара с момента предоставления очищенного от таможенных пошлин при ввозе товара в распоряжение покупателя на пристани (причале) в согласованном порту назначения. Все расходы и риски, включая уплату налогов, пошлин и иных сборов, по доставке товара в согласованное место несет продавец.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Данный термин не может применяться, если продавец прямо или косвенно не в состоянии получить импортную лицензию.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Если между сторонами будет согласовано, что таможенная очистка ввозимого товара и оплата связанных с этим пошлин возлагается на покупателя, вместо слов «с оплатой пошлины», следует написать «без оплаты пошлины».</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Если стороны согласились об исключении из обязательств продавца оплаты, некоторых связанных с импортом расходов (таких как налог на добавленную стоимость — VAT), то это должно быть прямо указано путем добавления соответствующих слов, например: «Поставка с пристани, без оплаты VAT, наименование порта назначения».</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Данный термин может быть использован при перевозках товара морским путем или внутренним водным транспортом.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana" color="#9D0A0F"><span class="LineHeight1-5">DDU Delivered Duty Unpaid Поставка без оплаты пошлины (Название Места Назначения)</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">«Поставка без оплаты пошлины (наименование пункта назначения)» означает, что продавец считается выполнившим свои обязанности с момента доставки товара в согласованный пункт в стране ввоза. Продавец должен нести расходы по оплате всех связанных с этим расходов и нести все риски, падающие на товар (исключая уплату налогов, при ввозе товара), а также все расходы и риски, в связи с выполнением таможенных формальностей.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">При невыполнении покупателем в срок обязанности по очистке товара при ввозе на него возлагаются все дополнительные расходы и он несет все дополнительные риски.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Если между сторонами будет согласовано, что таможенная очистка возлагается на продавца и он несет связанные с этим расходы и риски, то должно быть прямо указано путем добавления соответствующих слов.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Если между сторонами будет согласовано, что оплата взимаемых в связи с возмещением расходов (например, налога на добавленную стоимость, VAT), возлагается на продавца, то это должно быть прямо указано путем добавления соответствующих слов: «Поставка без оплаты пошлины, с оплатой VAT (согласованный пункт назначения)».</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Данный термин может применяться независимо от способа перевозки товара.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana" color="#9D0A0F"><span class="LineHeight1-5">DDP Delivered Duty Paid Поставка с оплатой пошлины (Название Места Назначения) </span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">«Поставка с оплатой пошлины», означает, что продавец считается выполнившим свои обязанности с момента предоставления товара в согласованном пункте в стране ввоза. Продавец несет все риски и расходы, связанные с  ввозом товара, включая оплату налогов, пошлин и иных сборов, взимаемых при ввозе товаров. В то время как термин «С завода» возлагает на продавца минимальные обязанности, термин «Поставка с оплатой пошлины» предполагает максимальные обязанности продавца.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Данный термин не может использоваться, если продавец прямо или косвенно не может обеспечить получение импортной лицензии.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Если между сторонами согласовано, что на покупателя возлагаются обязанности по таможенной очистке ввозимого товара и оплате соответствующих пошлин, следует применить термин DDU.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Если между сторонами согласовано, что оплата некоторых взимаемых в связи с ввозом расходов (например, налога на добавленную стоимость, VAT), возлагается на продавца, то это должно быть прямо указано путем добавления соответствующих слов: «Поставка с оплатой пошлины, без оплаты VAT, (согласованный пункт назначения)».</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Данный термин может применяться независимо от способа перевозки товара.</span></font></p>

