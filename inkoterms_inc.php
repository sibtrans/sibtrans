<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<p style="text-align: justify;" align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5">Инкотермс — международные правила по толкованию наиболее широко используемых торговых терминов в области внешней торговли. Международные торговые термины представляют собой стандартные условия договора международной купли-продажи, которые определены заранее в международнопризнанном документе.</span></font></p>

