<p style="text-align: justify;" align="justify"><font color="#9D0A0F"><b><font size="2" face="Verdana"><span class="LineHeight1-5">ГРУППА E</span></font></b></font></p>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">EXW Ex Works С завода (Название Места) </span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Термин «С завода» означает, что продавец выполняет свое обязательство по поставке, когда он передает товар в распоряжение покупателя на своем предприятии (заводе, фабрике, складе и т.п.). В частности, он не отвечает за погрузку товара на транспортное средство, предоставленное покупателем, а также за таможенную очистку товара для ввоза, если не оговорено иное, покупатель несет все расходы и риски в связи с перевозкой товара от предприятия продавца к месту назначения. Таким образом, данный термин возлагает на продавца минимальные обязанности. Он не применим, если покупатель не в состоянии осуществить сам или обеспечить выполнение экспортных формальностей. В этом случае должен использоваться термин FCA.</span></font></p>
</div>

