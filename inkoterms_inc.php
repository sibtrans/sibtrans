</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana" color="#9D0A0F"><span class="LineHeight1-5">CIF Cost, Insurance And Freight Стоимость, страхование, фрахт (... Название Порта Назначения) </span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">«Стоимость, страхование, фрахт» означает, что продавец несет те же обязанности, что по условиям CFR, однако он должен обеспечить морское страхование от риска гибели или повреждения товара во время перевозки, продавец заключает договор страхования и выплачивает страховщику страховую премию.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Покупатель должен принять к сведению, что согласно условиям CIF от продавца требуется лишь обеспечение страхования на минимальных условиях.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Данный термин может применяться лишь при перевозке товара морским или внутренним водным транспортом. В случаях, когда момент перехода товара через поручни судна не имеет практического значения, например, при перевозках на условиях ролл-он или ролл-офф, или при перевозках в контейнерах, более целесообразно применение термина CIP.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana" color="#9D0A0F"><span class="LineHeight1-5">СРТ Carriage Paid To Перевозка Оплачена до (Название Места Назначения) </span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">«Перевозка оплачена до» означает, что продавец оплачивает фрахт за перевозку товара до указанного места назначения.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Риск гибели или повреждения товара, а также любого увеличения издержек, возникающего после передачи товара перевозчику, переходит с продавца на покупателя, когда товар передан в распоряжение перевозчика.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Перевозчик означает любое лицо, которое согласно договору перевозки обязуется осуществить перевозку или организовать перевозку морским или воздушным путем, железнодорожным или автомобильным транспортом, по внутренним водным линиям или смешанной перевозки.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">При осуществлении перевозки несколькими перевозчиками в согласованном направлении, риск гибели или повреждения переходит, когда товар передан в распоряжение первого перевозчика.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Согласно термину СРТ, в обязанности продавца входит осуществление необходимой для вывоза таможенной очистки товара.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Данный термин может применяться для всех видов перевозки, включая смешанные перевозки.</span></font></p>
