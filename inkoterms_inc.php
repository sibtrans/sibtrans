<p style="text-align: justify;" align="justify"><font color="#9D0A0F"><b><font size="2" face="Verdana"><span class="LineHeight1-5">ГРУППА F</span></font></b></font></p>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">FCA Free Carrier Франко Перевозчик (Название Места)</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Термин «Франко перевозчик» означает, что продавец считается выполнившим свое обязательство по поставке товара, прошедшего таможенную очистку для ввоза, с момента передачи его в распоряжение перевозчика в обусловленном пункте. При отсутствии указания покупателя о таком пункте, продавец вправе определить такой пункт сам среди ряда подобных пунктов, где перевозчик принимает товар в свое распоряжение. В тех случаях, когда согласно коммерческой практике для заключения договора перевозки необходимо содействие продавца (при перевозке товара воздушным или железнодорожным транспортом), он может действовать в таких случаях за счет и на риск покупателя.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Данный термин может быть использован при осуществлении перевозки любым видом транспорта, включая смешанные перевозки.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Перевозчик означает любое лицо, которое на основании договора перевозки обязуется осуществить или обеспечить перевозку товара по железной дороге, автомобильным, воздушным, морским или внутренним водным транспортом или в смешанной перевозке. Если покупатель уполномочивает продавца поставить товар лицу, не являющемуся перевозчиком, например, экспедитору, продавец считается выполнившим свою обязанность по поставе товара с момента передачи его на хранение данному лицу.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Транспортный терминал означает железнодорожную станцию, причал, контейнерный терминал или двор, пункт по принятию различных видов грузов, то есть любых контейнеров и/или поддонов, независимо от применения ISO, трейлеров, прицепов, оборудования типа ро-ро, igloos, применяемых любыми средствами транспорта.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana" color="#9D0A0F"><span class="LineHeight1-5">FAS Free Alongside Ship Свободно вдоль борта судна (Название Порта Отгрузки) </span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Свободно вдоль борта судна означает, что продавец считается выполнившим свое обязательство по поставке, когда товар размещен вдоль борта судна на причале или на лихтерах в согласованном пункте отгрузки. Это означает, что с этого момента покупатель должен нести все расходы и риски гибели или повреждения товара. По условиям ФАС (FAS) на покупателя возлагается обязанность по очистке товара лот пошлин для его вывоза. Данный термин не следует применять, когда покупатель прямо или косвенно не в состоянии обеспечить выполнение таможенных формальностей.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana" color="#9D0A0F"><span class="LineHeight1-5">FOB Free On Board Свободно на борту (Название Порта Отгрузки)</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">«Свободно на борту» означает, что продавец считается выполнившим свое обязательство по поставке с момента перехода товара через поручни судна в порту отгрузки. Это означает, что с этого момента покупатель должен нести все расходы и риски гибели или повреждения товара. По условиям FOB на покупателя возлагаются обязанности по очистке товара от пошлин для его экспорта.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Данный термин может применяться лишь при перевозке товара морским или внутренним водным транспортом. В случаях, когда момент перехода товара через поручни судна не имеет практического значения, например, при перевозках на условиях ролл-он или ролл-офф или при перевозках в контейнерах, более целесообразно применение термина FAS.</span></font></p>
</div>

