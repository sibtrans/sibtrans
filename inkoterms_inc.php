<p style="text-align: justify;" align="justify"><font color="#9D0A0F"><b><font size="2" face="Verdana"><span class="LineHeight1-5">ГРУППА C</span></font></b></font></p>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana" color="#9D0A0F"><span class="LineHeight1-5">CFR Cost And Freight Стоимость и фрахт (Название Порта Назначения)</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">«Стоимость и фрахт» означает, что продавец обязан оплатить расходы и фрахт, необходимые для доставки товара в указанный порт назначения, однако риск утраты или повреждения товара, а также риск любого увеличения расходов, возникающих после перехода товаром борта судна, переходит с продавца на покупателя в момент перехода товара через поручни судна в порту отгрузки. По условиям CFR на продавца возлагаются обязанности по очистке товара от пошлин для его вывоза.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Данный термин может применяться лишь при перевозке товара морским или внутренним водным транспортом. В случаях, когда момент перехода товара через поручни судна не имеет практического значения, например, при перевозках на условиях ролл-он или ролл-офф, или при перевозках в контейнерах, более целесообразно применение термина CPT.</span></font></p>
</div>

