<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana" color="#9D0A0F"><span class="LineHeight1-5">СIР Carriage And Insurance Paid To Перевозка и страхование оплачены до (Название Места Назначения) </span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">«Провозная плата и страхование оплачены до» означает, что продавец несет те же обязанности, что и согласно термину СРТ, но с тем дополнением, что продавец должен обеспечить транспортное страхование от рисков гибели или повреждения товара во время перевозки. Продавец заключает договор страхования и оплачивает страховую премию. Покупатель должен принять к сведению, что согласно термину СIP от продавца требуется лишь обеспечение страхования на минимальных условиях.</span></font></p>
</div>

<p align="justify"><font size="2" face="Verdana"><span class="LineHeight1-5"> </span></font></p>

<div style="text-align: justify;" align="justify">
  <p><font size="2" face="Verdana"><span class="LineHeight1-5">Согласно данным условиям, продавец обязан обеспечить таможенную очистку товара для экспорта. Данный термин может использоваться при перевозке товара любым видом транспорта, включая смешанные перевозки.</span></font></p>
</div>

